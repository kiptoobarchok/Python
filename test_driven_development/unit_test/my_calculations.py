#!/usr/bin/python3


class Calculations:
    def __init__(self, a, b):
        self.a = a
        self.b = b

    def get_sum(self):
        return self.a + self.b
    
    def get_diff(self):
        return self.a - self.b
    
    def get_product(self):
        return self.a * self.b
    
    def get_div(self):
        return self.a / self.b
    
    def get_mod(self):
        return self.a % self.b
    

    